class Swap {
    public static void main(String args[]){
        int x=10,a=15,z;
        System.out.println("Before Swap");
        System.out.println("x = "+x);
        System.out.println("a = "+a);
        z=x;
        x=a;
        a=z;
        System.out.println("After Swap");
        System.out.println("x = "+x);
        System.out.println("a = "+a);
    }
}
