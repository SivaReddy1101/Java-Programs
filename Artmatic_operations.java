class ArtmaticOperations {
    public static void main(String args[]) {
        int a = Integer.parseInt(args[0]);
        int b = Integer.parseInt(args[1]);
        int sum,multi,sub,div;
        sum = a + b;
        multi = a * b;
        sub = a - b;
        div = a / b;
        System.out.println("Sum of the given number:"+sum);
        System.out.println("Sum of the given number:"+sub);
        System.out.println("Sum of the given number:"+multi);
        System.out.println("Sum of the given number:"+div);
    }
}
